		<h2> frequently asked questions </h2>

		<p> Here you will find the questions we get asked most often about mindcloud. </p>

		<h3> Is Mindcloud live? </h3>

		<p> Technically yes. While you are able to create an account a contribute content, however, we still consider this
			a prototype with much work left. At this time, we're working on gauging interest in what Mindcloud has to offer.
		</p>

		<h3>What's the difference between Mindcloud and services like Quoraâ„¢, Yahoo Answers, and Reddit?</h3>

		<p> The difference between Mindcloud and a question-and-answer type of website is that we are hoping to facilitate
			product-based solutions. In essence, we are looking for the type of questions to which there are no existing 
			answers to. In this way we hope to sustain innovation, rather than introduce a service oriented at solving problems
			specific to an individual.
		</p>

		<h3> How protected are the ideas that I put on Mindcloud? </h3>

		<p> At this point and time, all content posted on Mindcloud is covered by the Creative Commons Attribution Licence,
			which can be found <a href="https://creativecommons.org/licenses/by/3.0/legalcode">here</a>.

			Down the road, we hope to implement a more robust system that allows you to share your ideas while maintaining 
			ownership of any intellectual property.
		</p>

		<h3> What makes a good problem? </h3>

		<p> The most fruitful problems are going to be the ones that apply to multiple people and relate to a specific issue. 
			Keeping the title simple, with something like "A is B" or "Could we get A to do B", and including a solid
			description might work best.
			<br/><br/>
			Also, staying down to earth helps. Everyone wants to be able to teleport across the country, but the mindcloud
			community is going to be hardpressed to come up with a solution for that one.
		</p>

